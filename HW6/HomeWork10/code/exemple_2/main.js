const квадратЧисла = число => {
    const округленноеЧисло = Math.floor(число); // Округляем в меньшую сторону
    return округленноеЧисло * округленноеЧисло; // Возвращаем квадрат
};

// Примеры использования:
console.log(квадратЧисла(4.7));  // Выведет: 16
console.log(квадратЧисла(5.2));  // Выведет: 25
console.log(квадратЧисла(-3.9)); // Выведет: 9