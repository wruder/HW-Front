// Создаем массив из 10 случайных чисел от 1 до 100
const массив = Array.from({ length: 10 }, () => Math.floor(Math.random() * 100) + 1);

// Инициализируем максимальное и минимальное значения первым элементом массива
let максимальное = массив[0];
let минимальное = массив[0];

// Находим максимальное и минимальное значения в массиве
for (let i = 1; i < массив.length; i++) {
    if (массив[i] > максимальное) максимальное = массив[i];
    if (массив[i] < минимальное) минимальное = массив[i];
}

// Выводим массив, максимальное и минимальное значения
console.log("Массив:", массив);
console.log("Максимальное значение:", максимальное);
console.log("Минимальное значение:", минимальное);