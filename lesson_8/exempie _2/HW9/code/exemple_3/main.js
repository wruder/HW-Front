// Объявляем массив из 10 элементов и заполняем его случайными числами от 1 до 75
const массив = Array.from({ length: 10 }, () => Math.floor(Math.random() * 75) + 1);

// Инициализируем минимальное и максимальное значения первым элементом массива
let максимальное = массив[0];
let минимальное = массив[0];

// Находим максимальное и минимальное значения в массиве
for (let i = 1; i < массив.length; i++) {
    if (массив[i] > максимальное) максимальное = массив[i];
    if (массив[i] < минимальное) минимальное = массив[i];
}

// Находим сумму минимального и максимального значений
const сумма = минимальное + максимальное;

// Выводим массив, минимальное и максимальное значения, а также их сумму
console.log("Массив:", массив);
console.log("Минимальное значение:", минимальное);
console.log("Максимальное значение:", максимальное);
console.log("Сумма минимального и максимального значений:", сумма);